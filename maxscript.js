


let circleRadius = prompt('Введіть радіус кола: ');
let areaCircle = Math.PI * circleRadius * circleRadius;
alert ('Площа круга з радіусом ' + circleRadius + ' становить: ' + areaCircle );




let distance = prompt('Введіть відстань між містами в км:');
let time = prompt('За який час ви хочете добратися до місця призначення?');
let speed = distance / time;
alert('Вам потрібно рухатися зі швидкістю ' + speed + ' км/год');



